void main() {

  // Variable

  var namaVariabel = 10;

  print(namaVariabel);

  print("----------");

  // String

  var bahasa = "Dart";
  var bahasa1 = "Dart";

  print(bahasa);
  print(bahasa1);

  print("----------");

  // Number

  int angka = 10;
  double desimal = 10;

  print(angka);
  print(desimal);

  print("----------");

  // Boolean

  bool asli = true;
  bool palsu = false;

  print(asli);
  print(palsu);

  print("----------");

  // Dynamic

  dynamic fleksibel = 10;
  dynamic fleksibel2 = "Kalimat";
  dynamic fleksibel3 = 10.0;
  dynamic fleksibel4 = true;

  print(fleksibel);
  print(fleksibel2);
  print(fleksibel3);
  print(fleksibel4);

  print("----------");

  // Operator

  var nilaiPertama = 4;
  var nilaiKedua = 10;

  var tambah = nilaiPertama + nilaiKedua;
  var tambah2 = (4 + 10);
  print(tambah);
  print(tambah2);

  print(5 + 5);
  print(5 - 5);
  print(5 * 5);
  print(5 / 5);
  print(5 % 5);

  print("----------");

  // Equal Operator

  var seratus = 100;
  print(seratus == 5);
  print(seratus == 100);

  // Not Equal

  var sifat = "rajin";
  print(sifat != "rajin");

  print("----------");

  // Kurang dari lebih dari

  print(seratus < 100);
  print(seratus > 20);
  print(seratus >= 99);
  print(seratus <= 100);

  // OR ( || )

  print("------OR------");

  print(true || true);
  print(true || false);
  print(true || false || false);
  print(true || false || false);

  // AND ( && )

  print("------AND------");

  print(true && true);
  print(true && false);
  print(true && true && false);

}